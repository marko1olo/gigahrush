export function resolveNpcBarkLogRadiusMeters(radiusMeters?: number): number {
  if (!Number.isFinite(radiusMeters)) return DEFAULT_NPC_BARK_LOG_RADIUS_METERS;
  return Math.max(0, Math.min(MAX_NPC_BARK_LOG_RADIUS_METERS, Math.round(radiusMeters!)));
}

export function setNpcBarkLogContext(context: NpcBarkLogContext = {}): void {
  npcBarkLogContext = {
    listener: context.listener,
    radiusMeters: resolveNpcBarkLogRadiusMeters(context.radiusMeters),
    dist2: context.dist2,
  };
}

function resolveNpcBarkContext(context?: NpcBarkLogContext): NpcBarkLogContext {
  if (!context) return npcBarkLogContext;
  return {
    listener: context.listener ?? npcBarkLogContext.listener,
    radiusMeters: context.radiusMeters ?? npcBarkLogContext.radiusMeters,
    dist2: context.dist2 ?? npcBarkLogContext.dist2,
    signal: context.signal,
    hud: context.hud,
    hudPriority: context.hudPriority,
  };
}

function npcBarkDistanceForLog(e: Entity, context: NpcBarkLogContext = npcBarkLogContext): number | null | undefined {
  const listener = context.listener;
  if (!listener) return undefined;
  const d2 = context.dist2
    ? context.dist2(listener.x, listener.y, e.x, e.y)
    : (listener.x - e.x) * (listener.x - e.x) + (listener.y - e.y) * (listener.y - e.y);
  if (!Number.isFinite(d2)) return undefined;
  const distance = Math.sqrt(Math.max(0, d2));
  if (distance > resolveNpcBarkLogRadiusMeters(context.radiusMeters)) return null;
  return Math.max(0, Math.round(distance));
}

export function pushNpcLogMessage(
  e: Entity,
  msgs: Msg[],
  time: number,
  text: string,
  color = '#cca',
  context?: NpcBarkLogContext,
): boolean {
  const resolvedContext = resolveNpcBarkContext(context);
  const distanceMeters = npcBarkDistanceForLog(e, resolvedContext);
  if (distanceMeters === null) return false;
  const entry = msg(text, time, color, distanceMeters);
  if (resolvedContext.hud !== undefined) entry.hud = resolvedContext.hud;
  if (resolvedContext.hudPriority !== undefined) entry.hudPriority = resolvedContext.hudPriority;
  msgs.push(entry);
  return true;
}

export function pushNpcBarkMessage(
  e: Entity,
  msgs: Msg[],
  time: number,
  line: string,
  color = '#cca',
  context?: NpcBarkLogContext,
): boolean {
  if (!e.name) return false;
  const resolvedContext = resolveNpcBarkContext(context);
  const signal = resolvedContext.signal ?? 'ambient';
  return pushNpcLogMessage(e, msgs, time, `${e.name}: ${line}`, color, {
    ...resolvedContext,
    hud: resolvedContext.hud ?? barkSignalShowsHud(signal),
    hudPriority: resolvedContext.hudPriority ?? barkSignalHudPriority(signal),
  });
}

export function bark(e: Entity, msgs: Msg[], time: number, pool: string[], poolF: string[], chance: number, color = '#cca'): void {
  if (Math.random() > chance) return;
  if (!e.name) return;
  const selected = selectContextBarkPool(e, pool, poolF);
  const line = pickReadonly(selected);
  const last = lastBarkByEntity.get(e.id);
  if (last && time - last.time < BARK_ENTITY_COOLDOWN_S && last.text === line) return;
  const signal = barkSignalForPool(pool);
  const pack = resolveNpcPackageForEntity(e);
  const seed = e.alifeId ?? e.id;
  const packageFallback = pack && !isUnsafeMarkovBarkSignal(signal)
    ? selectNpcCuratedFallback(pack, 'bark_ambient', seed)
    : undefined;
  const routed = selected.length > 1
    ? generateMarkovBark({
      actor: {
        id: e.id,
        name: e.name,
        faction: e.faction,
        occupation: e.occupation,
      },
      signal,
      exactFallback: packageFallback ?? line,
      seed,
      repeatIndex: Math.floor(time),
      tags: pack ? npcPackageSpeechContextTags(pack, e, 'bark') : undefined,
      routeSpeech: routeBarkSpeech,
    })
    : undefined;
  const heard = pushNpcBarkMessage(e, msgs, time, routed?.text ?? line, color, { signal });
  if (!heard) return;
  lastBarkByEntity.set(e.id, { time, text: line });
  if (lastBarkByEntity.size > MAX_BARK_COOLDOWNS) pruneBarkCooldowns();
}

function barkSignalShowsHud(signal: NpcBarkSignal): boolean {
  return signal === 'alert' || signal === 'witness';
}

function barkSignalHudPriority(signal: NpcBarkSignal): number {
  switch (signal) {
    case 'alert': return 80;
    case 'witness': return 58;
    case 'lead': return 38;
    default: return 8;
  }
}

function barkSignalForPool(pool: readonly string[]): NpcBarkSignal {
  if (pool === BARK_COMBAT_START || pool === BARK_WOUNDED || pool === BARK_FLEE || pool === BARK_HIDE) return 'alert';
  if (pool === BARK_KILL) return 'witness';
  if (pool === BARK_ARRIVE) return 'lead';
  return 'ambient';
}

function selectContextBarkPool(e: Entity, pool: string[], poolF: string[]): readonly string[] {
  const hpRatio = e.hp !== undefined && e.maxHp ? e.hp / Math.max(1, e.maxHp) : 1;
  if (pool === BARK_WOUNDED && hpRatio < 0.35) return factionContextPool(CONTEXT_BARK_FACTION_WOUNDED, e) ?? CONTEXT_BARK_WOUNDED;
  if (pool === BARK_FLEE && ((e.needs?.sleep ?? 100) < 20 || hpRatio < 0.6)) return CONTEXT_BARK_FEAR;
  if (pool === BARK_HIDE) {
    const cleanupPool = technicalCleanupBarkPool(e);
    if (cleanupPool && Math.random() < 0.45) return cleanupPool;
    const factionHide = factionContextPool(CONTEXT_BARK_FACTION_HIDE, e);
    if (factionHide && Math.random() < 0.65) return factionHide;
    if (Math.random() < 0.45) return CONTEXT_BARK_SAMOSBOR_HIDE;
  }
  if (pool === BARK_GENERIC) {
    const food = e.needs?.food ?? 100;
    const water = e.needs?.water ?? 100;
    if (food < 25 && water < 25 && Math.random() < 0.7) return CONTEXT_BARK_SHORTAGE;
    if (food < 30 && Math.random() < 0.6) return CONTEXT_BARK_HUNGER;
    if (water < 30 && Math.random() < 0.6) return CONTEXT_BARK_THIRST;
    const cleanupPool = technicalCleanupBarkPool(e);
    if (cleanupPool && Math.random() < 0.65) return cleanupPool;
    if (occupationHasProfileTag(e.occupation, 'medical') && Math.random() < 0.65) return BARK_MEDICAL_QUEUE;
    if (occupationHasProfileTag(e.occupation, 'alcoholic') && Math.random() < 0.55) return BARK_GENERIC_ALCOHOLIC;
    if (occupationHasProfileTag(e.occupation, 'cult') && Math.random() < 0.65) return BARK_GENERIC_LOW_CULT;
    if (e.faction === Faction.CULTIST && Math.random() < 0.45) return BARK_GENERIC_CULTIST_FRAY;
    const occupationAmbient = occupationContextPool(CONTEXT_BARK_OCCUPATION_AMBIENT, e);
    if (occupationAmbient && Math.random() < 0.45) return occupationAmbient;
    const factionAmbient = factionContextPool(CONTEXT_BARK_FACTION_AMBIENT, e);
    if (factionAmbient && Math.random() < 0.6) return factionAmbient;
  }
  if (pool === BARK_ARRIVE) {
    const cleanupPool = technicalCleanupBarkPool(e);
    if (cleanupPool) return cleanupPool;
  }
  if (pool === BARK_KILL) {
    if (e.faction === Faction.LIQUIDATOR) return BARK_LIQUIDATOR_KILL;
    if (e.faction === Faction.WILD) return BARK_WILD_KILL;
    if (e.faction === Faction.CULTIST) return BARK_CULTIST_KILL;
    if (isTraderOccupation(e.occupation)) return BARK_TRADER_KILL;
    if (e.faction === Faction.CITIZEN) return BARK_CITIZEN_KILL;
  }
  if (pool === BARK_COMBAT_START && e.faction === Faction.LIQUIDATOR) return BARK_LIQUIDATOR_COMBAT;
  if (pool === BARK_COMBAT_START && e.faction === Faction.WILD) return BARK_WILD_COMBAT;
  if (pool === BARK_COMBAT_START && e.faction === Faction.CULTIST) return BARK_CULTIST_COMBAT;
  if (pool === BARK_COMBAT_START && isTraderOccupation(e.occupation)) return BARK_TRADER_COMBAT;
  if (pool === BARK_COMBAT_START && e.faction === Faction.CITIZEN) return BARK_CITIZEN_COMBAT;
  if (pool === BARK_COMBAT_START && e.faction !== undefined) return CONTEXT_BARK_FACTION[e.faction] ?? (e.isFemale ? poolF : pool);
  return e.isFemale ? poolF : pool;
}

function factionContextPool(pools: Record<number, readonly string[]>, e: Entity): readonly string[] | undefined {
  return e.faction !== undefined ? pools[e.faction] : undefined;
}

function occupationContextPool(pools: Record<number, readonly string[]>, e: Entity): readonly string[] | undefined {
  return e.occupation !== undefined ? pools[e.occupation] : undefined;
}

function technicalCleanupBarkPool(e: Entity): readonly string[] | undefined {
  if (e.faction === Faction.LIQUIDATOR && occupationHasProfileTag(e.occupation, 'medical')) return SANITARY_CLEANUP_BARKS;
  if (isTechnicalOccupation(e.occupation)) return TECHNICAL_CLEANUP_BARKS;
  if (e.faction === Faction.LIQUIDATOR && Math.random() < 0.35) return SANITARY_CLEANUP_BARKS;
  return undefined;
}

function isTechnicalOccupation(occupation: Occupation | undefined): boolean {
  return occupationHasProfileTag(occupation, 'technical');
}

function isTraderOccupation(occupation: Occupation | undefined): boolean {
  return occupationHasProfileTag(occupation, 'trader');
}

function pickReadonly(arr: readonly string[]): string {
  return arr[Math.floor(Math.random() * arr.length)];
}

function pruneBarkCooldowns(): void {
  let oldestId = -1;
  let oldestTime = Infinity;
  for (const [id, entry] of lastBarkByEntity) {
    if (entry.time < oldestTime) {
      oldestTime = entry.time;
      oldestId = id;
    }
  }
  if (oldestId >= 0) lastBarkByEntity.delete(oldestId);
}

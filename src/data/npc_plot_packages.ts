interface MainPlotNpcPackageInput {
  id: string;
  displayName: string;
  publicLine: string;
  sex: CharacterSex;
  age: number;
  faction: Faction;
  occupation: Occupation;
  sprite: number;
  spriteScale?: number;
  npcVisualId?: string;
  homeFloorKey: string;
  presence?: NpcPackagePresence;
  hp: number;
  maxHp: number;
  level?: number;
  money: number;
  accountRubles?: number;
  speed: number;
  weapon?: string;
  inventory: readonly Item[];
  talkLines: readonly string[];
  talkLinesPost: readonly string[];
  talkQuestResponse?: string | readonly string[];
  voiceTags?: readonly string[];
  specialRoutineId?: string;
}

function packageInventory(items: readonly Item[]): readonly Item[] {
  const out: Item[] = [];
  for (const item of items) {
    if (out.length >= NPC_PACKAGE_INVENTORY_CAP) break;
    const def = ITEMS[item.defId];
    const stackMax = def ? getStack(def) : Math.max(1, Math.floor(item.count));
    let remaining = Math.max(1, Math.floor(item.count));
    while (remaining > 0 && out.length < NPC_PACKAGE_INVENTORY_CAP) {
      const count = Math.min(remaining, stackMax);
      const stack: Item = { defId: item.defId, count };
      if (item.data !== undefined) stack.data = item.data;
      out.push(stack);
      remaining -= count;
    }
  }
  return out;
}

function runtimeSpeed(speed: number): number {
  return Math.max(0.1, Math.min(20, speed));
}

function plotNpcPackage(input: MainPlotNpcPackageInput): NpcPackageDef {
  return {
    version: 1,
    id: input.id,
    kind: 'plot',
    identity: {
      displayName: input.displayName,
    },
    bio: { publicLine: input.publicLine },
    demographics: {
      sex: input.sex,
      age: input.age,
    },
    affiliation: {
      faction: input.faction,
      occupation: input.occupation,
      roleId: `plot:${input.id}`,
    },
    rpg: { level: input.level ?? 1 },
    wealth: {
      cashRubles: input.money,
      accountRubles: input.accountRubles,
    },
    loadout: {
      weapon: input.weapon,
      inventory: packageInventory(input.inventory),
    },
    social: {},
    visual: {
      sprite: input.sprite,
      spriteScale: input.spriteScale,
      npcVisualId: input.npcVisualId,
    },
    placement: {
      homeFloorKey: input.homeFloorKey,
      presence: input.presence ?? 'anchor',
      mobility: 'fixed_home',
    },
    speech: {
      voiceTags: input.voiceTags,
      talkLines: input.talkLines,
      talkLinesPost: input.talkLinesPost,
      talkQuestResponse: input.talkQuestResponse,
    },
    runtime: {
      hp: input.hp,
      maxHp: input.maxHp,
      speed: runtimeSpeed(input.speed),
      ...(input.specialRoutineId ? { specialRoutineId: input.specialRoutineId } : {}),
    },
    content: {
      plotNpcId: input.id,
      debugPath: `plot:${input.id}`,
    },
    tags: ['plot', 'authored', 'main_plot', input.id],
  };
}

